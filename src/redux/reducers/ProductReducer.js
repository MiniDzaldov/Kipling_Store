export const productReducer = (state = initialState, action) => {
    switch (action.type) {
        case ("ADDQTY"):
            const add_state = [...state]
            add_state[action._idProduct].qty += 1;
            add_state[action._idProduct].cart -= 1;
            return add_state;
        case ("DECREASEQTY"):
            const dec_state = [...state]
            dec_state[parseInt(action._idProduct)].qty -= 1;
            dec_state[action._idProduct].cart += 1;
            return dec_state;
    }
    return state;
}

